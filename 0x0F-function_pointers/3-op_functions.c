#include "3-calc.h"

/**
 * op_add - Functiont that add two integers
 * @a: integer 1.
 * @b: integer 2.
 * Return: integer.
 */

int op_add(int a, int b)
{ return (a + b); }

/**
 * op_sub - Functiont that sub two integers
 * @a: integer 1.
 * @b: integer 2.
 * Return: integer.
 */

int op_sub(int a, int b)
{ return (a - b); }
/**
 * op_mul- Functiont that multiply two integers
 * @a: integer 1.
 * @b: integer 2.
 * Return: integer.
 */

int op_mul(int a, int b)
{ return (a * b); }

/**
 * op_div - Functiont that div two integers
 * @a: integer 1.
 * @b: integer 2.
 * Return: integer.
 */

int op_div(int a, int b)
{ return (a / b); }
/**
 * op_mod - Functiont that mod two integers
 * @a: integer 1.
 * @b: integer 2.
 * Return: integer.
 */

int op_mod(int a, int b)
{ return (a % b); }
